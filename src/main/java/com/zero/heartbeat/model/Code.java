package com.zero.heartbeat.model;

public class Code {
	private String		code_cd;
	private String		code_category;
	private String		code_meaning;
	
	public String getCode_cd() {
		return code_cd;
	}
	public void setCode_cd(String code_cd) {
		this.code_cd = code_cd;
	}
	public String getCode_category() {
		return code_category;
	}
	public void setCode_category(String code_category) {
		this.code_category = code_category;
	}
	public String getCode_meaning() {
		return code_meaning;
	}
	public void setCode_meaning(String code_meaning) {
		this.code_meaning = code_meaning;
	}
}
